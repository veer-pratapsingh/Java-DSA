import java.util.*;
class ReadKeyboard
{
    public static void main(String arg[])
    {
        Scanner sc=new Scanner(System.in);
        int x=sc.nextInt();
        System.out.println(x);
        float y=sc.nextFloat();
        System.out.println(y);
        String z=sc.next();
        System.out.println(z);
        String a=sc.nextLine();
        System.out.println(a);
    }
}